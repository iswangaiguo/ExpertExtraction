package com.expert.model;

public class ExpertCopy {
	private int th_id;
	private String th_name;
	private String th_sex;
	private int th_age;
	private String th_field;
	private String th_professional_title;
	public int getTh_id() {
		return th_id;
	}
	public void setTh_id(int th_id) {
		this.th_id = th_id;
	}
	public String getTh_name() {
		return th_name;
	}
	public void setTh_name(String th_name) {
		this.th_name = th_name;
	}
	public String getTh_sex() {
		return th_sex;
	}
	public void setTh_sex(String th_sex) {
		this.th_sex = th_sex;
	}
	public int getTh_age() {
		return th_age;
	}
	public void setTh_age(int th_age) {
		this.th_age = th_age;
	}
	public String getTh_field() {
		return th_field;
	}
	public void setTh_field(String th_field) {
		this.th_field = th_field;
	}
	public String getTh_professional_title() {
		return th_professional_title;
	}
	public void setTh_professional_title(String th_professional_title) {
		this.th_professional_title = th_professional_title;
	}
	
}
